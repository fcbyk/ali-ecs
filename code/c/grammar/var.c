#include <stdio.h>

/**
 * 变量
 * 作用：给一段指定的内存空间起名，方便操作这段内存
 * 数据类型 变量名 = 初始值;
 * 数据类型的作用是告诉计算机给给该变量分配多大的内存
 */

// #region g
// 全局变量：从程序开始到结束一直存在，存储在全局/静态存储区。
int g_a = 10;
int g_b = 10;

// 全局常量：只读，生命周期与程序一致，存储在只读数据段。
const int G_A = 10;
const int G_B = 10;

// 静态全局变量：只能在当前文件中访问，生命周期与程序一致。
static int sg_a = 20;
static int sg_b = 20;

int *globalPtr = &g_a; // 全局指针变量
// #endregion g

// #region l
void a() {
    // 局部变量：仅在函数调用时存在，存储在栈区。
    int a = 10;
    int b = 10;

    // 局部常量：与局部变量类似，生命周期仅限函数执行期间。
    const int A = 100;
    const int B = 200;

    // 静态局部变量：作用域为函数内部，但生命周期贯穿程序始终，存储在静态存储区。
    static int s_a = 30;
    static int s_b = 40;

    // 寄存器变量：存储在寄存器中（如果硬件允许），生命周期仅限函数调用。
    // 寄存器变量不能在全局作用域声明
    register int r_a = 50;

    // 局部指针变量：存储地址，生命周期与声明它的作用域一致。
    int *p = &a;

    s_a++; // 静态局部变量保留值，下一次调用时 s_a = 31。
}

void b() {
    a();
    a(); // 静态局部变量 s_a 将累积增值。
}
// #endregion l