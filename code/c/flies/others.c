/*
目标文件 (Object Files)
 -文件扩展名：.o 或 .obj
 -作用：由编译器生成的中间文件，包含机器代码，用于链接生成可执行文件。

可执行文件 (Executable Files)
 -文件扩展名：.exe（Windows） 或 无扩展名（Linux/Unix）。
 -作用：程序的最终运行文件。

库文件 (Library Files)
 -文件扩展名：.a（静态库，Linux）、.so（动态库，Linux）、.lib（静态库，Windows）、.dll（动态库，Windows）。
 -作用：封装的可重用代码，可以被多个程序共享使用。
*/